public function findBySearch(SearchData $searchData)
    {
        $data = $this->createQueryBuilder('v')
            ->addOrderBy('v.createdAt', 'DESC');
        if (!empty($searchData->q)) {
            $data = $data
                ->andWhere('v.title LIKE :q')
                ->setParameter('q', "%{$searchData->q}%");
        }
        return $data
            ->getQuery();
